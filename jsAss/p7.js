// Adding 2 Strings
function addStrings(str1, str2) {
    return str1 + str2;
}
console.log(addStrings("Hello, ", "World!")); 

// Adding 2 Arrays
function addArrays(arr1, arr2) {
    return arr1.concat(arr2);
}
console.log(addArrays([1, 2, 3], [4, 5, 6])); 